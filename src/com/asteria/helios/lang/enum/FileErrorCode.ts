/**
 * The <code>FileErrorCode</code> enum contains common error codes exposed by the Node.js API.
 */
export enum FileErrorCode {

    /**
     * Specified an error of the type of 'ENOENT'.
     */
    ENOENT = 'ENOENT',
    
    /**
     * Specified an error of the type of 'EISDIR'.
     */
    EISDIR = 'EISDIR'
}