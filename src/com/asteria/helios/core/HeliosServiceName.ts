/**
 * The <code>HeliosServiceName</code> enum defines all the names for the default services of the Helios server.
 */
export enum HeliosServiceName {

    /**
     * The reference to access the <code>ProcessorRegistry</code> service.
     */
    PROCESSOR_REGISTRY = 'processor-registry',

    /**
     * The reference to access the <code>TemplateRegistry</code> service.
     */
    TEMPLATE_REGISTRY = 'template-registry',

    /**
     * The reference to access the <code>RouteConfigRegistry</code> service.
     */
    ROUTE_CONFIG_REGISTRY = 'route-config-registry',
    
    /**
     * The reference to access the <code>ModuleRegistry</code> service.
     */
    MODULE_REGISTRY = 'module-registry',
}